/**
 * Manager for query caches across multiple collections.
 *
 * Provides a centralized way to manage query caches for all collections
 * in a database with shared configuration and global operations.
 *
 * @example
 * ```typescript
 * const manager = new QueryCacheManager({ maxEntries: 500 });
 *
 * // Get or create a cache for a collection
 * const usersCache = manager.getCache('users');
 *
 * // Invalidate all caches (e.g., on database-level operations)
 * manager.invalidateAll();
 *
 * // Get combined statistics
 * const stats = manager.getAllStats();
 * ```
 */
export class QueryCacheManager {
  private caches: Map<string, QueryResultCache<Document>> = new Map();
  private globalOptions: QueryCacheOptions;

  constructor(options?: QueryCacheOptions) {
    this.globalOptions = options ?? {};
  }

  /**
   * Get or create a query cache for a collection.
   *
   * @param collectionName - Name of the collection
   * @param options - Optional cache options (merged with global options)
   * @returns The query cache for the collection
   */
  getCache<T extends Document = Document>(
    collectionName: string,
    options?: QueryCacheOptions
  ): QueryResultCache<T> {
    let cache = this.caches.get(collectionName);

    if (!cache) {
      cache = new QueryResultCache<Document>(collectionName, {
        ...this.globalOptions,
        ...options,
      });
      this.caches.set(collectionName, cache);
    }

    return cache as QueryResultCache<T>;
  }

  /**
   * Check if a cache exists for a collection.
   *
   * @param collectionName - Name of the collection
   */
  hasCache(collectionName: string): boolean {
    return this.caches.has(collectionName);
  }

  /**
   * Remove a cache for a collection.
   *
   * @param collectionName - Name of the collection
   */
  removeCache(collectionName: string): boolean {
    return this.caches.delete(collectionName);
  }

  /**
   * Invalidate all caches.
   * Useful for database-level operations that affect all collections.
   */
  invalidateAll(): void {
    for (const cache of this.caches.values()) {
      cache.invalidate();
    }
  }

  /**
   * Clear all caches.
   */
  clearAll(): void {
    for (const cache of this.caches.values()) {
      cache.clear();
    }
  }

  /**
   * Get statistics for all caches.
   */
  getAllStats(): Map<string, QueryCacheStats> {
    const stats = new Map<string, QueryCacheStats>();
    for (const [name, cache] of this.caches) {
      stats.set(name, cache.getStats());
    }
    return stats;
  }

  /**
   * Get combined statistics across all caches.
   */
  getCombinedStats(): {
    totalEntries: number;
    totalHits: number;
    totalMisses: number;
    overallHitRate: number;
    totalInvalidations: number;
    totalMemoryBytes: number;
    cacheCount: number;
  } {
    let totalEntries = 0;
    let totalHits = 0;
    let totalMisses = 0;
    let totalInvalidations = 0;
    let totalMemoryBytes = 0;

    for (const cache of this.caches.values()) {
      const stats = cache.getStats();
      totalEntries += stats.entries;
      totalHits += stats.hits;
      totalMisses += stats.misses;
      totalInvalidations += stats.invalidations;
      totalMemoryBytes += stats.memoryBytes;
    }

    const total = totalHits + totalMisses;
    const overallHitRate = total > 0 ? (totalHits / total) * 100 : 0;

    return {
      totalEntries,
      totalHits,
      totalMisses,
      overallHitRate: Math.round(overallHitRate * 100) / 100,
      totalInvalidations,
      totalMemoryBytes,
      cacheCount: this.caches.size,
    };
  }

  /**
   * Enable or disable all caches.
   */
  setEnabledAll(enabled: boolean): void {
    for (const cache of this.caches.values()) {
      cache.setEnabled(enabled);
    }
  }

  /**
   * Prune expired entries from all caches.
   * Returns the total number of entries pruned.
   */
  pruneAll(): number {
    let totalPruned = 0;
    for (const cache of this.caches.values()) {
      totalPruned += cache.prune();
    }
    return totalPruned;
  }

  /**
   * Get the number of collections with caches.
   */
  get size(): number {
    return this.caches.size;
  }
}

