/**
 * Query result cache for a single collection.
 *
 * Caches query results with LRU eviction and TTL-based expiration.
 * Automatically invalidates cache entries when the collection is modified.
 *
 * @example
 * ```typescript
 * const cache = new QueryResultCache({ maxEntries: 500, ttlMs: 60000 });
 *
 * // Check cache before querying
 * const cached = cache.get(filter, options);
 * if (cached) {
 *   return cached;
 * }
 *
 * // Execute query and cache results
 * const results = await executeQuery(filter, options);
 * cache.set(filter, options, results);
 *
 * // Invalidate on write
 * cache.invalidate();
 * ```
 */
export class QueryResultCache<T extends Document = Document> {
  private cache: LRUCache<string, QueryCacheEntry<T>>;
  private enabled: boolean;
  private invalidationCount: number = 0;
  private collectionName: string;
  private ttlMs: number;

  constructor(collectionName: string, options?: QueryCacheOptions) {
    this.collectionName = collectionName;
    this.enabled = options?.enabled !== false;
    this.ttlMs = options?.ttlMs ?? DEFAULT_QUERY_CACHE_TTL_MS;

    const cacheOptions: LRUCacheOptions<string, QueryCacheEntry<T>> = {
      maxSize: options?.maxEntries ?? DEFAULT_QUERY_CACHE_MAX_ENTRIES,
      maxMemoryBytes: options?.maxMemoryBytes ?? DEFAULT_QUERY_CACHE_MAX_MEMORY_BYTES,
      ttlMs: this.ttlMs,
      name: options?.name ?? `query-cache-${collectionName}`,
      metrics: options?.metrics,
      sizeEstimator: (entry) => entry.estimatedSize,
    };

    this.cache = new LRUCache(cacheOptions);
  }

  /**
   * Get cached query results.
   *
   * @param filter - Query filter
   * @param options - Find options
   * @returns Cached results if found and valid, undefined otherwise
   */
  get(filter?: Filter<T>, options?: FindOptions): WithId<T>[] | undefined {
    if (!this.enabled) return undefined;

    const key = generateCacheKey(filter as Filter<Document>, options);
    const entry = this.cache.get(key);

    if (!entry) return undefined;

    // Return a shallow copy to prevent mutations affecting cached data
    return [...entry.results];
  }

  /**
   * Cache query results.
   *
   * @param filter - Query filter
   * @param options - Find options
   * @param results - Query results to cache
   */
  set(filter: Filter<T> | undefined, options: FindOptions | undefined, results: WithId<T>[]): void {
    if (!this.enabled) return;

    // Don't cache very large result sets to avoid memory pressure
    // Threshold: 10000 documents or estimated size > 10MB
    const estimatedSize = estimateEntrySize(results);
    if (results.length > 10000 || estimatedSize > 10 * 1024 * 1024) {
      return;
    }

    const key = generateCacheKey(filter as Filter<Document>, options);
    const entry: QueryCacheEntry<T> = {
      results: [...results], // Store a copy
      cachedAt: Date.now(),
      query: { filter, options },
      estimatedSize,
    };

    this.cache.set(key, entry);
  }

  /**
   * Check if a query result is cached.
   *
   * @param filter - Query filter
   * @param options - Find options
   * @returns True if the query result is cached
   */
  has(filter?: Filter<T>, options?: FindOptions): boolean {
    if (!this.enabled) return false;
    const key = generateCacheKey(filter as Filter<Document>, options);
    return this.cache.has(key);
  }

  /**
   * Invalidate all cached query results for this collection.
   * Call this when the collection is modified (insert, update, delete).
   */
  invalidate(): void {
    this.cache.clear();
    this.invalidationCount++;
  }

  /**
   * Invalidate a specific cached query.
   *
   * @param filter - Query filter
   * @param options - Find options
   */
  invalidateQuery(filter?: Filter<T>, options?: FindOptions): void {
    if (!this.enabled) return;
    const key = generateCacheKey(filter as Filter<Document>, options);
    this.cache.delete(key);
  }

  /**
   * Enable or disable the cache.
   */
  setEnabled(enabled: boolean): void {
    this.enabled = enabled;
    if (!enabled) {
      this.cache.clear();
    }
  }

  /**
   * Check if the cache is enabled.
   */
  isEnabled(): boolean {
    return this.enabled;
  }

  /**
   * Get cache statistics.
   */
  getStats(): QueryCacheStats {
    const lruStats = this.cache.getStats();

    return {
      entries: lruStats.size,
      maxEntries: lruStats.maxSize,
      hits: lruStats.hits,
      misses: lruStats.misses,
      hitRate: lruStats.hitRate,
      invalidations: this.invalidationCount,
      memoryBytes: lruStats.memoryBytes,
      maxMemoryBytes: lruStats.maxMemoryBytes,
    };
  }

  /**
   * Reset cache statistics.
   */
  resetStats(): void {
    this.cache.resetStats();
    this.invalidationCount = 0;
  }

  /**
   * Get the collection name this cache is associated with.
   */
  getCollectionName(): string {
    return this.collectionName;
  }

  /**
   * Get the number of entries in the cache.
   */
  get size(): number {
    return this.cache.size;
  }

  /**
   * Clear all cached entries.
   */
  clear(): void {
    this.cache.clear();
  }

  /**
   * Prune expired entries from the cache.
   * Returns the number of entries pruned.
   */
  prune(): number {
    return this.cache.prune();
  }
}

