/**
 * Query Result Cache
 *
 * Provides caching for query results to improve read performance.
 * Features:
 * - Cache key generation from query parameters (filter, options)
 * - TTL-based expiration
 * - Size limits (both entry count and memory)
 * - Cache invalidation on writes
 * - Per-collection cache isolation
 * - Statistics for monitoring cache effectiveness
 *
 * @module query/result-cache
 */

import type { Document, WithId, Filter, FindOptions } from '../types.js';
import { LRUCache, type LRUCacheOptions } from '../utils/lru-cache.js';
import type { MetricsCollector } from '../metrics/index.js';

// ============================================================================
// Types
// ============================================================================

/**
 * Cache entry containing query results and metadata
 */
export interface QueryCacheEntry<T extends Document = Document> {
  /** Cached query results */
  results: WithId<T>[];
  /** Timestamp when the entry was created */
  cachedAt: number;
  /** Query that produced these results */
  query: {
    filter?: Filter<T>;
    options?: FindOptions;
  };
  /** Estimated size in bytes */
  estimatedSize: number;
}

/**
 * Configuration options for the query result cache
 */
export interface QueryCacheOptions {
  /** Maximum number of cached queries per collection (default: 1000) */
  maxEntries?: number;
  /** Maximum memory in bytes for the cache (default: 50MB) */
  maxMemoryBytes?: number;
  /** TTL for cache entries in milliseconds (default: 5 minutes) */
  ttlMs?: number;
  /** Cache name for metrics labeling */
  name?: string;
  /** Metrics collector for recording cache statistics */
  metrics?: MetricsCollector;
  /** Enable/disable the cache (default: true) */
  enabled?: boolean;
}

/**
 * Statistics about the query cache
 */
export interface QueryCacheStats {
  /** Number of cached entries */
  entries: number;
  /** Maximum entries allowed */
  maxEntries: number;
  /** Number of cache hits */
  hits: number;
  /** Number of cache misses */
  misses: number;
  /** Hit rate percentage */
  hitRate: number;
  /** Total invalidations due to writes */
  invalidations: number;
  /** Estimated memory usage in bytes */
  memoryBytes: number;
  /** Maximum memory allowed */
  maxMemoryBytes?: number;
}

// ============================================================================
// Constants
// ============================================================================

/** Default maximum entries per collection cache */
export const DEFAULT_QUERY_CACHE_MAX_ENTRIES = 1000;

/** Default TTL for query cache entries (5 minutes) */
export const DEFAULT_QUERY_CACHE_TTL_MS = 5 * 60 * 1000;

/** Default maximum memory for query cache (50MB) */
export const DEFAULT_QUERY_CACHE_MAX_MEMORY_BYTES = 50 * 1024 * 1024;

// ============================================================================
// Cache Key Generation
// ============================================================================

/**
 * Generate a stable cache key from query parameters.
 *
 * The key is generated by:
 * 1. Serializing the filter and options to JSON
 * 2. Sorting object keys for deterministic output
 * 3. Hashing the result for a compact key
 *
 * @param filter - Query filter
 * @param options - Find options
 * @returns A stable cache key string
 */
export function generateCacheKey(filter?: Filter<Document>, options?: FindOptions): string {
  // Create a normalized query object with sorted keys
  const normalizedQuery = {
    f: sortObjectKeys(filter || {}),
    o: sortObjectKeys(normalizeOptions(options)),
  };

  // Serialize to JSON - this will be deterministic due to key sorting
  const serialized = JSON.stringify(normalizedQuery);

  // Use a fast hash for the cache key
  return fastHash(serialized);
}

/**
 * Normalize find options for caching.
 * Removes options that shouldn't affect caching (like callbacks).
 */
function normalizeOptions(options?: FindOptions): Record<string, unknown> {
  if (!options) return {};

  // Only include options that affect query results
  const normalized: Record<string, unknown> = {};

  if (options.projection) normalized.projection = sortObjectKeys(options.projection);
  if (options.sort) normalized.sort = sortObjectKeys(options.sort);
  if (options.limit !== undefined) normalized.limit = options.limit;
  if (options.skip !== undefined) normalized.skip = options.skip;
  if (options.hint) {
    normalized.hint = typeof options.hint === 'string'
      ? options.hint
      : sortObjectKeys(options.hint);
  }

  return normalized;
}

/**
 * Sort object keys recursively for deterministic serialization.
 */
function sortObjectKeys(obj: unknown): unknown {
  if (obj === null || obj === undefined) return obj;
  if (Array.isArray(obj)) return obj.map(sortObjectKeys);
  if (typeof obj !== 'object') return obj;

  const sorted: Record<string, unknown> = {};
  const keys = Object.keys(obj as Record<string, unknown>).sort();

  for (const key of keys) {
    sorted[key] = sortObjectKeys((obj as Record<string, unknown>)[key]);
  }

  return sorted;
}

/**
 * Fast hash function for cache keys.
 * Uses a simple but effective hash algorithm for string keys.
 */
function fastHash(str: string): string {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32-bit integer
  }
  // Convert to hex and ensure positive
  return (hash >>> 0).toString(16);
}

/**
 * Estimate the memory size of a cache entry in bytes.
 */
function estimateEntrySize<T extends Document>(results: WithId<T>[]): number {
  // Base overhead for the entry object
  let size = 128;

  // Estimate size of each document
  for (const doc of results) {
    size += estimateDocumentSize(doc);
  }

  return size;
}

/**
 * Estimate the memory size of a document.
 */
function estimateDocumentSize(doc: unknown): number {
  if (doc === null || doc === undefined) return 8;

  const type = typeof doc;

  if (type === 'boolean') return 4;
  if (type === 'number') return 8;
  if (type === 'string') return (doc as string).length * 2 + 48;
  if (type === 'bigint') return 16;

  if (Array.isArray(doc)) {
    let size = 48 + doc.length * 8;
    for (const item of doc) {
      size += estimateDocumentSize(item);
    }
    return size;
  }

  if (type === 'object') {
    let size = 48;
    for (const key of Object.keys(doc as Record<string, unknown>)) {
      size += key.length * 2 + 48;
      size += estimateDocumentSize((doc as Record<string, unknown>)[key]);
    }
    return size;
  }

  return 16;
}

