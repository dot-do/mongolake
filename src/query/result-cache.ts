/**
 * Create a query result cache for a collection.
 *
 * @param collectionName - Name of the collection
 * @param options - Cache configuration options
 * @returns A new QueryResultCache instance
 */
export function createQueryCache<T extends Document = Document>(
  collectionName: string,
  options?: QueryCacheOptions
): QueryResultCache<T> {
  return new QueryResultCache<T>(collectionName, options);
}

/**
 * Create a query cache manager.
 *
 * @param options - Default cache configuration options
 * @returns A new QueryCacheManager instance
 */
export function createQueryCacheManager(options?: QueryCacheOptions): QueryCacheManager {
  return new QueryCacheManager(options);
}
